#include "corewar.h"

int						isnumberstr(char *s)
{
	size_t		len;
	size_t		i;

	i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		if (!ft_isdigit(s[i++]))
			return (0);
	}
	return (1);
}
