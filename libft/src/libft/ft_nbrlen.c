#include "libft.h"

int			ft_nbrlen(int nb)
{
	int		len;
	int		cpy;

	if (nb == 0)
		return (1);
	len = 0;
	cpy = nb;
	while (cpy != 0)
	{
		cpy /= 10;
		len++;
	}
	return (nb > 0 ? len : len + 1);
}
